let a = 5;
let b = 6;

console.log(a > b, "es mayor");
console.log(a >= b, "es mayor o igual");
console.log(a < b, "es menor");
console.log(a <= b, "es menor o igual");
console.log(a == 5, "es igual");
console.log(a != b, "es distinto");
console.log(a == '5', "es el mismo");
console.log(a === '5', "es el mismo y el mismo tipo");
console.log(a !== '5', "son distintos tipos");
