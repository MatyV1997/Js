let a = 8;
let b = 10;

console.log(a + b, "suma"); //adición
console.log(a - b, "resta"); //resta
console.log(a * b, "multiplicación"); //multiplicación
console.log(a / b, "división"); //división
console.log(a - b, "resto"); //módulo
console.log(a ** b, "potencia"); //potencia

//Incrementar
console.log(++a); //incrementó la variable antes de que apaarezca en consola
console.log(a++); //incremento la variable después de que aparece en consola
console.log(a); //la tengo que volver a imprimir para notar el cambio

//Decrementar
console.log(--b); //decrementó la variable antes de que apaarezca en consola
console.log(b--); //decremento la variable después de que aparece en consola
console.log(b); //la tengo que volver a imprimir para notar el cambio
